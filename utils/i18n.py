def get_text(key: str, **kwargs) -> str:
    """获取翻译后的文本。优先从 session_state 读取语言配置。"""
    lang = st.session_state.get("language", "zh")
    text = TRANSLATIONS.get(lang, TRANSLATIONS["zh"]).get(key, key)
    if kwargs:
        return text.format(**kwargs)
    return text
